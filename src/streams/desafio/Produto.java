package streams.desafio;

public class Produto {

	final String marca;
	final String nome;
	final double preco;
	final double desconto;
	
	Produto(String marca, String nome, double preco, double desconto){
		this.marca = marca;
		this.nome = nome;
		this.preco = preco;
		this.desconto = desconto;
	}
	
	
	
}
